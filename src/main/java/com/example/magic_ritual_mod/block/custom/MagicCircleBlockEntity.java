    public static void tick(Level level, BlockPos pos, BlockState state, MagicCircleBlockEntity be) {
        if (level.isClientSide) return;
        if (level.getGameTime() % EFFECT_INTERVAL != 0) return;

        BlockPos centerPos = findCenterBlock(level, pos);
        if (centerPos == null) return;

        BlockEntity centerBE = level.getBlockEntity(centerPos);
        if (!(centerBE instanceof MagicCenterBlockEntity mcbe) || !mcbe.hasFuel()) return;

        int radius = level.getBlockState(centerPos).getValue(MagicCenterBlock.RADIUS);
        CircleType type = state.getValue(MagicCircleBlock.CIRCLE_TYPE);

        int count = countSameTypeCircles(level, centerPos, radius, type);
        int amplifier = Math.max(0, count - 1);

        Holder<MobEffect> effectType = switch (type) {
            case ATTACK -> MobEffects.DAMAGE_BOOST;
            case DEFENSE -> MobEffects.DAMAGE_RESISTANCE;
            case REGENERATION -> MobEffects.REGENERATION;
        };

        if (ComboBlockEntity.isCirclePartOfActiveCombo(level, pos)) return;

        AABB area = new AABB(pos);

        for (LivingEntity entity : level.getEntitiesOfClass(LivingEntity.class, area)) {
            if (!mcbe.isAffectedByCircle(entity)) continue;

            entity.removeEffect(effectType);
            entity.addEffect(new MobEffectInstance(
                    effectType,
                    EFFECT_DURATION,
                    amplifier,
                    false,
                    true,
                    true
            ));
        }
    }

    public void clientTick(Level level, BlockPos pos, BlockState state) {

        Minecraft mc = Minecraft.getInstance();

        if (mc.level == null || mc.gameRenderer == null) {
            return;
        }

        Frustum frustum = mc.levelRenderer.getFrustum();

        AABB box = new AABB(pos).inflate(1);

        if (!frustum.isVisible(box)) {
            return;
        }

        if (level.getNearestPlayer(
                pos.getX() + 0.5,
                pos.getY() + 0.5,
                pos.getZ() + 0.5,
                32,
                false
        ) == null) {
            return;
        }

        if (level == null) return;

        double cx = pos.getX() + 0.5;
        double cy = pos.getY() + 0.05;
        double cz = pos.getZ() + 0.5;

        long time = level.getGameTime();
        double angle = Math.toRadians((time * 2) % 360);

        double cos = Math.cos(angle);
        double sin = Math.sin(angle);

        CircleType type = state.getValue(MagicCircleBlock.CIRCLE_TYPE);

        float r, g, b, intensity;

        if (type == CircleType.DEFENSE) {
            r = 0.2f;
            g = 0.4f;
            b = 1.0f;
            intensity = 0.15f;
        } else if (type == CircleType.REGENERATION) {
            r = 0.2f;
            g = 1.0f;
            b = 0.2f;
            intensity = 0.15f;
        } else {
            r = 1.0f;
            g = 0.0f;
            b = 0.0f;
            intensity = 0.15f;
        }

        double scale = 0.8;

        for (double[] p : POINTS) {
            double x = p[0] * scale;
            double z = p[1] * scale;

            double rx = x * cos - z * sin;
            double rz = x * sin + z * cos;

            level.addParticle(
                    new DustParticleOptions(new Vector3f(r, g, b), intensity),
                    cx + rx,
                    cy,
                    cz + rz,
                    0,
                    0,
                    0
            );
        }
    }

    private static BlockPos findCenterBlock(Level level, BlockPos pos) {
        for (int x = -SEARCH_RADIUS; x <= SEARCH_RADIUS; x++) {
            for (int z = -SEARCH_RADIUS; z <= SEARCH_RADIUS; z++) {
                double hDist = Math.sqrt(x * x + z * z);
                if (hDist > SEARCH_RADIUS + 0.5) continue;
                for (int y = -1; y <= 1; y++) {
                    BlockPos checkPos = pos.offset(x, y, z);
                    BlockState centerState = level.getBlockState(checkPos);
                    if (centerState.is(ModBlocks.MAGIC_CENTER_BLOCK.get())) {
                        int centerRadius = centerState.getValue(MagicCenterBlock.RADIUS);
                        double dx = checkPos.getX() - pos.getX();
                        double dz = checkPos.getZ() - pos.getZ();
                        if (Math.sqrt(dx * dx + dz * dz) <= centerRadius + 0.5) {
                            return checkPos;
                        }
                    }
                }
            }
        }
        return null;
    }

    private static int countSameTypeCircles(Level level, BlockPos centerPos, int radius, CircleType type) {
        int count = 0;
        for (int x = -radius; x <= radius; x++) {
            for (int z = -radius; z <= radius; z++) {
                double hDist = Math.sqrt(x * x + z * z);
                if (hDist > radius + 0.5) continue;
                for (int y = -1; y <= 1; y++) {
                    BlockPos checkPos = centerPos.offset(x, y, z);
                    BlockState state = level.getBlockState(checkPos);
                    if (state.is(ModBlocks.MAGIC_CIRCLE_BLOCK.get())) {
                        if (state.getValue(MagicCircleBlock.CIRCLE_TYPE) == type) {
                            count++;
                        }
                    }
                }
            }
        }
        return count;
    }
